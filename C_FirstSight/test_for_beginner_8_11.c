////9.2.3  scanf的占位符
// %c 字符
// %d 整数
// %f float类型浮点数
// %lf double类型浮点数
// %Lf long double类型浮点数
// %s 字符串
// %[] 在方括号中指定一组匹配的字符（比如%[0-9]），遇到不在集合之中的字符，匹配将会停止。
//【注意】上面所有占位符之中，除了%c以外，都会自动忽略起首的空白字符,%c不忽略空白字符，总是返回当前第一个字符，无论该字符是否为空格。
// 如果要强制跳过字符前的空白字符，可以写成scanf(" %c",&ch)。即%c前面加上一个空格，表示跳过零个或多个空白字符。
//【特别】下面要特别说一下占位符%s，它其实不能简单地等同于字符串。因为它的规则是，从当前第一个非空白字符开始读取，直到遇到空白字符为止。
//所以不能读取多个单词，除非多个占位符连用，这意味着它不适合用于读取可能包含空格的字符串，比如书名或曲名
//【注意】另外，scanf( )遇到%s占位符时会在字符串变量末尾存储一个空字符\0。
//【注意】scanf（）将字符串读入数组时，不会检查字符串是否超过了数组长度。所以，储存字符串时很可能会超过数组的边界，导致预想不到的结果。
//【解决】使用%s占位符时，应该指定读入字符串的最长长度，即写成%[m]s，其中的[m]是一个整数，表示读取字符串的最大长度，多余的字符将被丢弃。
