//【描述】用于读取用户的键盘输入，程序运行到这个语句时，会停下等待用户键入足够多的数值并按下回车键，scanf才会处理用户输入并将其存入变量
//【定义】它的原型定义在头文件stdio.h中
//【语法】scanf("%d",&i);
//【解释】它的第一个参数是格式字符串，里面会放置占位符（与用户输入数据类型相对应），它的其余参数就是存放用户输入的变量，格式字符串里有多少占位符，就有多少变量。
//【注意】变量前面必须加上&运算符（指针变量除外），因为scanf传递的不是值，而是地址，即变量i的地址指向用户输入的值；如果这里的变量是指针变量（比如字符串变量），那就不用加&。
//【注意】scanf处理数值占位符的时候会自动过滤空白字符，比如空格、制表符、换行符等等
